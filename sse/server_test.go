func Test3(t *testing.T) {
	r := req.New()
	pipe := pio.NewPipe()
	ctx, cancle := context.WithTimeout(t.Context(), time.Second*5)
	defer cancle()
	r.Reqf(req.Rval{
		Ctx: ctx,
		Url: "https://sse.dev/test",
		Header: map[string]string{
			`Referer`:    `https://sse.dev/testpage.html`,
			`Accept`:     `text/event-stream`,
			`Connection`: `keep-alive`,
		},
		SaveToPipe: pipe,
		Async:      true,
	})
	type AutoGenerated struct {
		Testing bool   `json:"testing"`
		SseDev  string `json:"sse_dev"`
		Msg     string `json:"msg"`
		Now     int64  `json:"now"`
	}
	var a AutoGenerated
	for f := range req.ResponSSE(pipe.Read) {
		if e := json.Unmarshal(f(pu.S2B("data")), &a); e != nil {
			t.Fatal(e)
		} else {
			t.Log(a)
		}
	}
}

