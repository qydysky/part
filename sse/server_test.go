func Test1(t *testing.T) {
	sseSer := NewServer()
	mq := sseSer.MQ()
	{
		mq.Pull_tag(map[string]func(*Umsg) (disable bool){
			`init`: func(u *Umsg) (disable bool) {
				t.Log(`init`, u.Id)
				return false
			},
			`recv`: func(u *Umsg) (disable bool) {
				t.Log(`recv`, u.Id)
				if string(u.Data) == "ok" {
					mq.Push_tag(`send`, u.Set([]byte("id"), []byte("123")))
					mq.Push_tag(`send`, u.Set([]byte("data"), []byte("456")))
					mq.Push_tag(`send`, u.End())
					mq.Push_tag(`close`, new(Umsg))
				}
				t.Log(`recvf`, u.Id)
				return false
			},
			`error`: func(u *Umsg) (disable bool) {
				t.Log(u.Err)
				mq.Push_tag(`close`, u)
				return false
			},
			`fin`: func(u *Umsg) (disable bool) {
				t.Log(`fin`, u.Id)
				return false
			},
		})
	}

	ser := web.New(&http.Server{Addr: "127.0.0.1:9000"})
	defer ser.Shutdown()
	ser.Handle(map[string]func(http.ResponseWriter, *http.Request){
		`/sse/`: sseSer.Handle,
	})

	time.Sleep(time.Second)
	r := req.New()
	r.Reqf(req.Rval{
		Url:     "http://127.0.0.1:9000/sse/",
		PostStr: "ok",
	})
	r.Respon(func(b []byte) error {
		for s, i := 0, 0; i < len(b); i++ {
			if b[i] == '\n' && i != s {
				t.Log(string(b[s:i]))
				s = i + 1
			}
		}
		return nil
	})
}

